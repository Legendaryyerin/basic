// ^, ?, =, ., *, &
//^ : 문자열의 시작을 나타냄
// * : 줄바꿈 문자를 제외한 아무 문자를 0회 이상 일치시킴.
// .+ : 줄바꿈 문자를 제외하고, 최소한 하나의 문자가 있는지 확인함.
// (?=.*[A-Z])는 문자열에 최소한 하나의 대문자가 포함되어 있는지 확인하는 양수형 전방 탐색
// ^: 입력 문자열의 시작을 나타냅니다.
/* (? = .*[A - Z]) : 전방 탐색을 수행하는 부분입니다.최소한 하나의 대문자[A - Z]가 문자열에 포함되어 있는지 확인합니다.
(? = : 전방 탐색의 시작을 나타냅니다.전방 탐색은 실제로 일치하는 부분을 소비하지 않고, 해당 패턴이 나타나는지 확인합니다.
    .*[A - Z] : 아무 문자(줄바꿈 문자 제외).*을 0개 이상 일치시킨 후, 최소한 하나의 대문자[A - Z]가 나타나는지 확인합니다.
    ) : 전방 탐색의 끝을 나타냅니다.
    . + : 최소한 하나의 문자(줄바꿈 문자 제외)가 있는지 확인합니다. + 는 앞에 있는 패턴이 1회 이상 반복되어야 함을 나타냅니다.
    $ : 입력 문자열의 끝을 나타냅니다.
    따라서, 정규 표현식 ^ (? = .*[A - Z]). + $은 다음과 같이 해석할 수 있습니다 :

"입력 문자열은 최소한 하나의 대문자를 포함해야 하며, 최소한 하나의 문자를 가지고 있어야 합니다. 문자열은 시작과 끝이 정의되어야 합니다."
*/

#include <stdio.h>
#include <stdbool.h>

bool containsUppercase(const char* str) {
    while (*str != '\0') {
        if (*str >= 'A' && *str <= 'Z') {
            return true;  // 대문자가 발견되면 true 반환
        }
        str++;
    }
    return false;  // 대문자가 발견되지 않으면 false 반환
}

int main() {
    char input[10];

    printf("문자열을 입력하세요: ");
    fgets(input, sizeof(input), stdin);

    if (containsUppercase(input)) {
        printf("입력한 문자열에는 최소한 하나의 대문자가 포함되어 있습니다.\n");
    }
    else {
        printf("입력한 문자열에 대문자가 포함되어 있지 않습니다.\n");
    }

    return 0;
}